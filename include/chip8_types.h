// 8 bits = 1 byte -> [0-255] or [0x00-0xFF]
// = unsigned char
typedef uint8_t u8;

// 16 bits = 2 bytes -> [0-65535] or [0x0000-0xFFFF]
// = unsigned short
typedef uint16_t u16;

// 32 bits = 4 bytes -> [0-4294967295] or [0x00000000-0xFFFFFFFF]
// = unsigned int
typedef uint32_t u32;

// 64 bits = 8 bytes
// -> [0-18446744073709551615] or [0x0000000000000000-0xFFFFFFFFFFFFFFFF]
// = unsigned long long
typedef uint64_t u64;